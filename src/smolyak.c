/************ sums of functions values sum(f(+/- x_i)) ********/

double fsum (int k)
{
  double dummy;
  if (k==0)
    {
      fsumme = 0;
      dummy=fsum(1);
    }
  else if (k==d+1)
    {
      fsumme+=(*f)(d,x);
    }
  else
    if (indeces[k]==0) /* differentiate 'central node', others */
      { 
	x[k-1]=0.5;
	dummy=fsum(k+1);
      }
    else
      {
	x[k-1]= xnu[indeces[k]][2*argind[k]+1];
	dummy=fsum(k+1);
	x[k-1] = 1-x[k-1];
	dummy=fsum(k+1);
      }  
  return fsumme;
}

/* get pt and wt from fsum1 -- C. Gu */
void fsum1 (int k, double *pt, double *wt)
{
    int i;
    if (k==0)       fsum1(1,pt,wt);
    else if (k==d+1)
    {
	for(i=0;i<d;i++) pt[d*count+i]=x[i];
	wt[count]=wtt;
	fsumme+=(*f)(d,x);
    }
    else
	if (indeces[k]==0)
	{ 
	    x[k-1]=0.5;
	    fsum1(k+1,pt,wt);
	}
	else
	{
	    x[k-1]= xnu[indeces[k]][2*argind[k]+1];
	    fsum1(k+1,pt,wt);
	    x[k-1] = 1-x[k-1];
	    fsum1(k+1,pt,wt);
	}  
}
/* get pt and wt from fsum1 -- C. Gu */


